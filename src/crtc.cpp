static inline byte getRAMByte(int video_address) {
   return *(pbRAM + video_address);
}

dword shiftLittleEndianDwordTriplet(dword val1, dword val2, dword val3, unsigned int byteShift) {
   if (byteShift == 0) {
      return val3;
   }
   if (byteShift == 4) {
      return val2;
   }
   if (byteShift >= 8) {
      return val1;
   }
   if (byteShift < 4) {
      int bitShift = 8 * byteShift;
      return (val3 << bitShift) | (val2 >> (32 - bitShift));
   }
   int bitShift = 8 * (byteShift - 4);
   return (val2 << bitShift) | (val1 >> (32 - bitShift));
}

